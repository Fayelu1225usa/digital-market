    private void add_prod_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_add_prod_btnMouseClicked
        // TODO add your handling code here:
        
        if (!String.valueOf(this.product_drg.getSelectedItem()).isBlank() && 
            !String.valueOf(this.supplier_drg.getSelectedItem()).isBlank() && 
            !this.actual_price_input.getText().trim().isEmpty() && 
            !this.quantity_input.getText().trim().isEmpty() && 
            Integer.parseInt(this.quantity_input.getText().trim())>0) {
            Product p = this.data.getBusiness().sd.findSupplier(String.valueOf(this.supplier_drg.getSelectedItem())).pc.findProduct(String.valueOf(this.product_drg.getSelectedItem()));
            if(p!=null){
                DefaultTableModel dtm = (DefaultTableModel) this.prod_table.getModel();
                String[] row = new String[5];
                row[0] = p.getID();
                row[1] = p.getDesc();
                row[2] = String.valueOf(this.supplier_drg.getSelectedItem());
                row[3] = this.actual_price_input.getText().trim();
                row[4] = this.quantity_input.getText().trim(); 
                dtm.addRow(row);
                dtm.fireTableDataChanged();
            }
            
        }
    }//GEN-LAST:event_add_prod_btnMouseClicked


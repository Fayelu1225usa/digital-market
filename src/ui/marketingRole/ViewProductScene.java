    private void updateSupplierDrg(){
        DefaultTableModel dtm = (DefaultTableModel) this.prod_table.getModel();
        dtm.setRowCount(0);
        if (!String.valueOf(this.supplier_drg.getSelectedItem()).isBlank()) {
            for (Product p : this.data.getBusiness().sd.findSupplier(this.supplier_drg.getSelectedItem().toString()).getProducts()) {
                String[] row = new String[]{p.getID(), p.getDesc(), String.valueOf(p.getFloor()), String.valueOf(p.getCeil()), String.valueOf(p.getTargetPrice())};
                dtm.addRow(row);
            }  
        }
        dtm.fireTableDataChanged();
    }

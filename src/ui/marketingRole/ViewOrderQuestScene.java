    private void updateSelectedTable(javax.swing.JTable table, ArrayList<Order> orders) {
        DefaultTableModel dtm = (DefaultTableModel) table.getModel();
        dtm.setRowCount(0);
        for (Order order : orders) {
            for (OrderItem item : order.getOrderItems()) {
                String[] row = new String[4];
                row[0] = item.getProduct().getDesc();
                row[1] = item.getProduct().getSupplier().person.getPersonId();
                row[2] = String.valueOf(item.getQuantity());
                row[3] = String.valueOf(item.calculatePricePerformance() * item.getQuantity());
                dtm.addRow(row);
            }
        }
        dtm.fireTableDataChanged();
    }

    private void updatePendingTable(javax.swing.JTable table, ArrayList<Order> orders) {

        DefaultTableModel dtm = (DefaultTableModel) table.getModel();
        dtm.setRowCount(0); 
        for (Order order : orders) {
            String[] row = new String[4];
            row[0] = order.getCustomer().getName();
            row[1] = String.join(",", order.getBundleAssignment().getMarketAssignMent().getMarket().getCharacters());
            row[2] = order.getBundleAssignment().getMarketAssignMent().getChannel().getType();
            row[3] = String.valueOf(order.getOrderTotal());
            dtm.addRow(row);
        }
        dtm.fireTableDataChanged();
    }

    private void updateTotalPrice() {
        int total = 0;
        for (Order order : this.selected_orders) {
            total += order.getOrderTotal();
        }
        this.total_selected_price.setText("$" + String.valueOf(total));
    }
    
    

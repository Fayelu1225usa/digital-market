    private void add_pending_order_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_add_pending_order_btnMouseClicked
        // TODO add your handling code here:
        int selected_row = pending_order_table.getSelectedRow();
        if (selected_row >= 0) {
            this.selected_orders.add(this.pending_orders.get(selected_row));
            this.pending_orders.remove(selected_row);
            //update selected table's rows
            updateSelectedTable(this.selected_orders_table, this.selected_orders);
            updatePendingTable(this.pending_order_table, this.pending_orders);
            updateTotalPrice();
        }


    }//GEN-LAST:event_add_pending_order_btnMouseClicked


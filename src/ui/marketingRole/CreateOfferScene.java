    private void submit_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submit_btnMouseClicked
        // TODO add your handling code here:
        int error = 0;
        //add markets
        ArrayList<String> markets = new ArrayList<String>();
        if(this.market_textfield.getText().isBlank()){
            error++;
        }
        else{ 
            markets = Util.wrapSpaces(this.market_textfield.getText().split(","));
        }
        
        //add channels
        ArrayList<String> channels = new ArrayList<String>();
        if(this.channel_textfield.getText().isBlank()){
            error++;
        }
        else{ 
            channels = Util.wrapSpaces(this.channel_textfield.getText().split(","));
        }
        
        //add products product id, product name, supplier id, actual price, quantity
        DefaultTableModel dtm = (DefaultTableModel) this.prod_table.getModel();
        ArrayList<String[]> products = new ArrayList<String[]>();
        if(dtm.getRowCount()<1){
            error++;
        }
        else {
            for(int row=0;row<dtm.getRowCount();row++){
                String[] items = new String[5];
                for(int col=0;col<dtm.getColumnCount();col++){  
                    items[col] = String.valueOf(dtm.getValueAt(row, col)); 
                }
                products.add(items);
            } 
        }
        
        if(this.ads_input.getText().isBlank()){
            error++;
        }
        
        if(error>0){
            JOptionPane.showMessageDialog(this.jf, "Please fill all fields.");
        }
        else {
            DataUtil.createSolutionBundleAssignment(this.data, markets, channels, products, (this.ads_input.getText()), this.data.getEmployees().findEmployee(this.account.getPerson().getPersonId()));
            JOptionPane.showMessageDialog(this.jf, "Successed!");
            cleanPanel();
            CreateOfferScene offer = new CreateOfferScene(this.jf, this.data, this.account);
            UiControl.createOffer(this.jf, offer);
        } 

    }//GEN-LAST:event_submit_btnMouseClicked


    private void updateSupplierDrg(){
        this.product_drg.removeAllItems();
        if (!String.valueOf(this.supplier_drg.getSelectedItem()).isBlank()) {
            if(String.valueOf(this.supplier_drg.getSelectedItem()).equals("All")){
                for(Supplier supplier:this.data.getBusiness().sd.supplier_list){
                    for (Product p : this.data.getBusiness().sd.findSupplier(supplier.person.getPersonId()).getProducts()) {
                        this.product_drg.addItem(p.getID());
                    }
                }
            }
            else{
                for (Product p : this.data.getBusiness().sd.findSupplier(this.supplier_drg.getSelectedItem().toString()).getProducts()) {
                    this.product_drg.addItem(p.getID());
                }
            }  
        }
    }

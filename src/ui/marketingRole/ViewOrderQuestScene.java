    private void cancel_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cancel_btnMouseClicked
        // TODO add your handling code here:
        this.selected_orders.clear();
        this.pending_orders = DataUtil.getPendingRequest(this.account.getPerson(), null, this.data);
        updateSelectedTable(this.selected_orders_table, this.selected_orders);
        updatePendingTable(this.pending_order_table, this.pending_orders);
        updateTotalPrice();
    }//GEN-LAST:event_cancel_btnMouseClicked

    private void submit_btnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submit_btnMouseClicked
        // TODO add your handling code here:
        if(this.selected_orders.size()>0){
          
            for (Order order : this.selected_orders) {
                order.setStatus("approved");
            }
            this.selected_orders.clear();
            updateSelectedTable(this.selected_orders_table, this.selected_orders);
            updateTotalPrice();
            JOptionPane.showMessageDialog(this.jf, "Successed!"); 
        }
        else {
            JOptionPane.showMessageDialog(this.jf, "Please select at least an order."); 
        }
        
    }//GEN-LAST:event_submit_btnMouseClicked


    private void update_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_btnActionPerformed
        // TODO add your handling code here:
        if(!String.valueOf(this.supplier_drg.getSelectedItem()).trim().isEmpty()){
            ArrayList<String[]> products = new ArrayList<String[]>();
           
            DefaultTableModel dtm = (DefaultTableModel) this.prod_table.getModel();
            int error = 0;
            for(int row=0;row<dtm.getRowCount();row++){
                String[] r = new String[5];
                
                if(!String.valueOf(dtm.getValueAt(row, 0)).trim().isEmpty()){
                    r[0] = String.valueOf(dtm.getValueAt(row, 0)).trim();
                }
                else{
                    error++;
                }
                if(!String.valueOf(dtm.getValueAt(row, 1)).trim().isEmpty()){
                    r[1] = String.valueOf(dtm.getValueAt(row, 1)).trim();
                }
                if(Util.isInteger(String.valueOf(dtm.getValueAt(row, 2)))){
                    r[2] =  String.valueOf(dtm.getValueAt(row, 2));
                }
                else{
                    error++;
                }
                if(Util.isInteger(String.valueOf(dtm.getValueAt(row, 3)))){
                    r[3] =  String.valueOf(dtm.getValueAt(row, 3));
                }
                else{
                    error++;
                }
                if(Util.isInteger(String.valueOf(dtm.getValueAt(row, 4)))){
                    r[4] = String.valueOf(dtm.getValueAt(row, 4));
                }
                else{
                    error++;
                }
                if(error<1){ 
                    products.add(r);
                }
                else{
                    break;
                }
            }
            if(error<1){
                DataUtil.updateProduct(this.data, products, String.valueOf(this.supplier_drg.getSelectedItem()).trim());
                JOptionPane.showMessageDialog(this.jf, "Successed!");
            }
        }
    }//GEN-LAST:event_update_btnActionPerformed

